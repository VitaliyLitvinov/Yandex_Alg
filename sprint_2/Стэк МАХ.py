class StackMax:
    def __init(self):
        self.stack = []
def push(self, value):
    self.stack.append(value)



def pop(self):
    if self.stack:
        return self.stack.pop()
    else:
        return None
def get_max(self):
    maximum = None
    for i in self.stack:
        if i > maximum: maximum = i
    return maximum

